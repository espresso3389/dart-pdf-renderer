void pdfiumCmykToRgb(int c, int m, int y, int k, List<int> rgb) {
  final fixC = c << 8;
  final fixM = m << 8;
  final fixY = y << 8;
  final fixK = k << 8;
  final cIndex = (fixC + 4096) >> 13;
  final mIndex = (fixM + 4096) >> 13;
  final yIndex = (fixY + 4096) >> 13;
  final kIndex = (fixK + 4096) >> 13;
  final start = _pdfiumCmykTableOffset(cIndex, mIndex, yIndex, kIndex);
  var fixR = _pdfiumCmykTable[start] << 8;
  var fixG = _pdfiumCmykTable[start + 1] << 8;
  var fixB = _pdfiumCmykTable[start + 2] << 8;

  var c1Index = fixC >> 13;
  if (c1Index == cIndex) c1Index = c1Index == 8 ? c1Index - 1 : c1Index + 1;
  var m1Index = fixM >> 13;
  if (m1Index == mIndex) m1Index = m1Index == 8 ? m1Index - 1 : m1Index + 1;
  var y1Index = fixY >> 13;
  if (y1Index == yIndex) y1Index = y1Index == 8 ? y1Index - 1 : y1Index + 1;
  var k1Index = fixK >> 13;
  if (k1Index == kIndex) k1Index = k1Index == 8 ? k1Index - 1 : k1Index + 1;

  final c1 = _pdfiumCmykTableOffset(c1Index, mIndex, yIndex, kIndex);
  final cRate = (fixC - (cIndex << 13)) * (cIndex - c1Index);
  fixR += (_pdfiumCmykTable[start] - _pdfiumCmykTable[c1]) * cRate ~/ 32;
  fixG +=
      (_pdfiumCmykTable[start + 1] - _pdfiumCmykTable[c1 + 1]) * cRate ~/ 32;
  fixB +=
      (_pdfiumCmykTable[start + 2] - _pdfiumCmykTable[c1 + 2]) * cRate ~/ 32;

  final m1 = _pdfiumCmykTableOffset(cIndex, m1Index, yIndex, kIndex);
  final mRate = (fixM - (mIndex << 13)) * (mIndex - m1Index);
  fixR += (_pdfiumCmykTable[start] - _pdfiumCmykTable[m1]) * mRate ~/ 32;
  fixG +=
      (_pdfiumCmykTable[start + 1] - _pdfiumCmykTable[m1 + 1]) * mRate ~/ 32;
  fixB +=
      (_pdfiumCmykTable[start + 2] - _pdfiumCmykTable[m1 + 2]) * mRate ~/ 32;

  final y1 = _pdfiumCmykTableOffset(cIndex, mIndex, y1Index, kIndex);
  final yRate = (fixY - (yIndex << 13)) * (yIndex - y1Index);
  fixR += (_pdfiumCmykTable[start] - _pdfiumCmykTable[y1]) * yRate ~/ 32;
  fixG +=
      (_pdfiumCmykTable[start + 1] - _pdfiumCmykTable[y1 + 1]) * yRate ~/ 32;
  fixB +=
      (_pdfiumCmykTable[start + 2] - _pdfiumCmykTable[y1 + 2]) * yRate ~/ 32;

  final k1 = _pdfiumCmykTableOffset(cIndex, mIndex, yIndex, k1Index);
  final kRate = (fixK - (kIndex << 13)) * (kIndex - k1Index);
  fixR += (_pdfiumCmykTable[start] - _pdfiumCmykTable[k1]) * kRate ~/ 32;
  fixG +=
      (_pdfiumCmykTable[start + 1] - _pdfiumCmykTable[k1 + 1]) * kRate ~/ 32;
  fixB +=
      (_pdfiumCmykTable[start + 2] - _pdfiumCmykTable[k1 + 2]) * kRate ~/ 32;

  rgb[0] = (math.max(fixR, 0) >> 8).clamp(0, 255).toInt();
  rgb[1] = (math.max(fixG, 0) >> 8).clamp(0, 255).toInt();
  rgb[2] = (math.max(fixB, 0) >> 8).clamp(0, 255).toInt();
}

int _pdfiumCmykTableOffset(int c, int m, int y, int k) =>
    (9 * 9 * 9 * c + 9 * 9 * m + 9 * y + k) * 3;
